/**
 * Accepts two numbers and returns their sum
 *
 * @param {Number} n1
 * @param {Number} n2
 */
function add(n1, n2) {
  return n1 + n2;
}

/**
 * Accepts two numbers and returns their product
 *
 * @param {Number} n1
 * @param {Number} n2
 */
function multiply(n1, n2) {
  return n1 * n2;
}

module.exports.add = add;
module.exports.multiply = multiply;
